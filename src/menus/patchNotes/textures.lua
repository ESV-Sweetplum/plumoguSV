function rasterizeVersionString(str, location, scale, colFn)
    local numberSpacing, letterSpacing, dotSpacing = 6, 8, 6

    local spacingMap = {
        ['v'] = letterSpacing,
        ['0'] = numberSpacing,
        ['1'] = numberSpacing,
        ['2'] = numberSpacing,
        ['3'] = numberSpacing,
        ['4'] = numberSpacing,
        ['5'] = numberSpacing,
        ['6'] = numberSpacing,
        ['7'] = numberSpacing,
        ['8'] = numberSpacing,
        ['9'] = numberSpacing,
        ['.'] = dotSpacing,
    }

    local pointer = location or vctr2(0)

    for char in str:gmatch('.') do
        local charTexture = getTexture(char)
        pointer = rasterizeTexture(charTexture, pointer, scale, char == '.' and 10 or nil, colFn)
        pointer.x = pointer.x + spacingMap[char]
    end
end

function rasterizeTexture(tex, ptr, scale, forcedSpace, colFn)
    local ctx = imgui.GetWindowDrawList()
    local maxX = -1
    local maxY = -1

    local col = color.int.white

    for i = 1, #tex, 2 do
        maxX = math.max(tex[i], maxX)
        maxY = math.max(tex[i + 1], maxY)
    end

    for i = 1, #tex, 8 do
        local p1 = ptr + scale * vector.New(tex[i], tex[i + 1] - maxY)
        local p2 = ptr + scale * vector.New(tex[i + 2], tex[i + 3] - maxY)
        local p3 = ptr + scale * vector.New(tex[i + 4], tex[i + 5] - maxY)
        local p4 = ptr + scale * vector.New(tex[i + 6], tex[i + 7] - maxY)

        if colFn then col = colFn(p1.x) end

        ctx.AddBezierCubic(p1, p2, p3, p4, col, 1)
    end

    return ptr + vector.New(forcedSpace or maxX, 0)
end

